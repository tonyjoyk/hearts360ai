function buildFragment(): string {
  const { stats, topFacilities } = getOverviewCardModel();
  const rows = topFacilities.map(facilityToOverviewRow);

  const metricCells = stats
    .map((s, i) => {
      const spark = SPARK_SVGS[i] ?? SPARK_SVGS[SPARK_SVGS.length - 1];
      return `                <div class="ds-metric">
                  <p class="ds-metric__label">${escapeHtml(s.label)}</p>
                  <p class="ds-metric__delta">${escapeHtml(fmtDelta(s.delta))}</p>
                  <p class="ds-metric__now">now <strong>${s.value}%</strong></p>
                  ${spark}
                </div>`;
    })
    .join("\n");

  const facItems = rows
    .map(
      (r) => `              <li class="ds-fac-item">
                <div class="ds-fac-row">
                  <span class="ds-fac-name">${escapeHtml(r.name)}</span>
                  <span class="ds-badge">${escapeHtml(r.badgeLabel)}</span>
                </div>
                <p class="ds-fac-desc">
                  ${escapeHtml(r.summaryLine)}
                </p>
              </li>`,
    )
    .join("\n");

  return `<!-- generated by hearts360ai/scripts/emit-hearts360-static-ds-card.ts — do not edit by hand -->
            <div class="ds-metrics-wrap">
              <div class="ds-metrics">
${metricCells}
              </div>
            </div>

            <p class="ds-fac-heading">Facilities that need attention</p>
            <ul class="ds-fac-list">
${facItems}
            </ul>
`;
}

const outPath = join(__dirname, "..", "..", "hearts360", "overview-ds-card-district-data.generated.html");
writeFileSync(outPath, buildFragment(), "utf8");
