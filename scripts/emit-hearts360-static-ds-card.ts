/**
 * Regenerates the HEARTS360 static `ds-card` metrics + facility list from
 * `src/data/facilities.ts` (same source as the embed panel).
 *
 * Usage (from repo root): `npx tsx scripts/emit-hearts360-static-ds-card.ts`
 *
 * Output: ../hearts360/overview-ds-card-district-data.generated.html
 * Paste that file’s contents over the matching block in hearts360/index.html
 * (between ds-metrics-wrap and the closing article), or diff to verify.
 */

import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { facilityToOverviewRow, getOverviewCardModel } from "../src/lib/overviewCardModel.ts";

const __dirname = dirname(fileURLToPath(import.meta.url));

function fmtDelta(delta: number): string {
  if (delta === 0) return "+0 pp";
  if (delta < 0) return `\u2212${Math.abs(delta)} pp`;
  return `+${delta} pp`;
}

function escapeHtml(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

